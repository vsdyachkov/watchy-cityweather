String normalize(String text)
{
    String normalized;
    normalized.reserve(text.length());
    bool previousWasSpace = false;
    for (uint16_t index = 0; index < text.length();)
    {
        uint8_t character = static_cast<uint8_t>(text.charAt(index));
        if (character < 32 || character == 127)
        {
            if (!previousWasSpace) { normalized += ' '; }
            previousWasSpace = true;
            index++;
            continue;
        }
        if (character < 0x80)
        {
            if (character == ' ')
            {
                if (!previousWasSpace) { normalized += ' '; }
                previousWasSpace = true;
            }
            else
            {
                normalized += static_cast<char>(character);
                previousWasSpace = false;
            }
            index++;
            continue;
        }
        uint8_t length = utf8CharLength(character);
        if (!hasValidUtf8Continuation(text, index, length))
        {
            if (!previousWasSpace) { normalized += ' '; }
            previousWasSpace = true;
            index++;
        }
        else
        {
            normalized += text.substring(index, index + length);
            previousWasSpace = false;
            index += length;
        }
    }
    normalized.trim();
    return normalized;
}

int16_t textWidth(const OpenSansCondensedFont &font, const String &text)
{
    int16_t width = 0;
    for (uint16_t index = 0; index < text.length();)
    {
        uint16_t codepoint;
        index = decodeUtf8Codepoint(text, index, &codepoint);
        const OpenSansCondensedGlyph *glyph = findGlyph(font, codepoint);
        if (glyph == nullptr) { glyph = findGlyph(font, '?'); }
        if (glyph != nullptr) { width += glyph->xAdvance; }
    }
    return width;
}

void drawText(Adafruit_GFX &display, const OpenSansCondensedFont &font, const String &text, int16_t x, int16_t baselineY, uint16_t color)
{
    int16_t cursorX = x;
    for (uint16_t index = 0; index < text.length();)
    {
        uint16_t codepoint;
        index = decodeUtf8Codepoint(text, index, &codepoint);
        const OpenSansCondensedGlyph *glyph = findGlyph(font, codepoint);
        if (glyph == nullptr) { glyph = findGlyph(font, '?'); }
        if (glyph == nullptr) { continue; }
        uint16_t bitIndex = 0;
        for (uint8_t row = 0; row < glyph->height; row++)
        {
            for (uint8_t column = 0; column < glyph->width; column++)
            {
                uint8_t byte = font.bitmap[glyph->bitmapOffset + bitIndex / 8];
                if ((byte & (0x80 >> (bitIndex & 0x07))) != 0)
                {
                    display.drawPixel(cursorX + glyph->xOffset + column, baselineY + glyph->yOffset + row, color);
                }
                bitIndex++;
            }
        }
        cursorX += glyph->xAdvance;
    }
}

void drawOutlinedText(
    Adafruit_GFX &display,
    const OpenSansCondensedFont &font,
    const String &text,
    int16_t x,
    int16_t baselineY,
    uint16_t textColor,
    uint16_t outlineColor
)
{
    for (int8_t offsetY = -2; offsetY <= 2; offsetY++)
    {
        for (int8_t offsetX = -2; offsetX <= 2; offsetX++)
        {
            if (offsetX == 0 && offsetY == 0) { continue; }
            drawText(display, font, text, x + offsetX, baselineY + offsetY, outlineColor);
        }
    }
    drawText(display, font, text, x, baselineY, textColor);
}

uint16_t fittingUtf8Length(const OpenSansCondensedFont &font, const String &text, int16_t maxWidth)
{
    uint16_t length = 0;
    while (length < text.length())
    {
        uint16_t codepoint;
        uint16_t nextLength = decodeUtf8Codepoint(text, length, &codepoint);
        if (textWidth(font, text.substring(0, nextLength)) > maxWidth) { break; }
        length = nextLength;
    }
    return length;
}

uint16_t wrappedLineLength(const OpenSansCondensedFont &font, const String &text, int16_t maxWidth)
{
    uint16_t length = 0;
    uint16_t lastSpace = 0;
    while (length < text.length())
    {
        uint16_t codepoint;
        uint16_t nextLength = decodeUtf8Codepoint(text, length, &codepoint);
        if (textWidth(font, text.substring(0, nextLength)) > maxWidth) { break; }
        if (codepoint == ' ') { lastSpace = length; }
        length = nextLength;
    }
    if (length == text.length()) { return length; }
    if (lastSpace > 0) { return lastSpace; }
    return fittingUtf8Length(font, text, maxWidth);
}

uint8_t printBlock(Adafruit_GFX &display, const OpenSansCondensedFont &font, String text, int16_t x, int16_t baselineY, int16_t maxWidth, uint8_t maxLines, uint16_t color, bool heavy)
{
    text = normalize(text);
    uint8_t line = 0;
    while (text.length() > 0 && line < maxLines)
    {
        uint16_t length = wrappedLineLength(font, text, maxWidth);
        if (length == 0) { break; }
        String currentLine = text.substring(0, length);
        currentLine.trim();
        int16_t lineBaseline = baselineY + line * font.lineHeight;
        drawText(display, font, currentLine, x, lineBaseline, color);
        if (heavy)
        {
            drawText(display, font, currentLine, x + 1, lineBaseline, color);
        }
        text = text.substring(length);
        text.trim();
        line++;
    }
    return line;
}

void printLine(Adafruit_GFX &display, const OpenSansCondensedFont &font, String text, int16_t x, int16_t baselineY, int16_t maxWidth, bool rightAlign, uint16_t color)
{
    text = normalize(text);
    uint16_t length = fittingUtf8Length(font, text, maxWidth);
    text = text.substring(0, length);
    text.trim();
    if (text.length() == 0) { return; }
    int16_t cursorX = x;
    if (rightAlign) { cursorX = x + maxWidth - textWidth(font, text); }
    drawText(display, font, text, cursorX, baselineY, color);
}

void printCentered(Adafruit_GFX &display, const OpenSansCondensedFont &font, String text, int16_t centerX, int16_t baselineY, int16_t maxWidth, uint16_t color)
{
    text = normalize(text);
    uint16_t length = fittingUtf8Length(font, text, maxWidth);
    text = text.substring(0, length);
    text.trim();
    if (text.length() == 0) { return; }
    int16_t cursorX = centerX - textWidth(font, text) / 2;
    drawText(display, font, text, cursorX, baselineY, color);
}

void printCenteredOutlined(
    Adafruit_GFX &display,
    const OpenSansCondensedFont &font,
    String text,
    int16_t centerX,
    int16_t baselineY,
    int16_t maxWidth,
    uint16_t textColor,
    uint16_t outlineColor
)
{
    text = normalize(text);
    uint16_t length = fittingUtf8Length(font, text, maxWidth);
    text = text.substring(0, length);
    text.trim();
    if (text.length() == 0) { return; }
    int16_t cursorX = centerX - textWidth(font, text) / 2;
    drawOutlinedText(display, font, text, cursorX, baselineY, textColor, outlineColor);
}
