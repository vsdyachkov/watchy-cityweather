uint8_t utf8CharLength(uint8_t leadByte)
{
    if (leadByte < 0x80) { return 1; }
    if ((leadByte & 0xE0) == 0xC0) { return 2; }
    if ((leadByte & 0xF0) == 0xE0) { return 3; }
    if ((leadByte & 0xF8) == 0xF0) { return 4; }
    return 0;
}

bool isUtf8Continuation(uint8_t value)
{
    return (value & 0xC0) == 0x80;
}

bool hasValidUtf8Continuation(const String &text, uint16_t start, uint8_t length)
{
    if (length == 0 || start + length > text.length()) { return false; }
    for (uint8_t offset = 1; offset < length; offset++)
    {
        if (!isUtf8Continuation(static_cast<uint8_t>(text.charAt(start + offset)))) { return false; }
    }
    return true;
}

uint16_t decodeUtf8Codepoint(const String &text, uint16_t index, uint16_t *codepoint)
{
    if (index >= text.length())
    {
        *codepoint = 0;
        return index;
    }
    uint8_t leadByte = static_cast<uint8_t>(text.charAt(index));
    if (leadByte < 0x80)
    {
        *codepoint = leadByte;
        return index + 1;
    }
    uint8_t length = utf8CharLength(leadByte);
    if (!hasValidUtf8Continuation(text, index, length) || length > 3)
    {
        *codepoint = '?';
        return index + 1;
    }
    uint32_t decoded = length == 2 ? leadByte & 0x1F : leadByte & 0x0F;
    for (uint8_t offset = 1; offset < length; offset++)
    {
        decoded = (decoded << 6) | (static_cast<uint8_t>(text.charAt(index + offset)) & 0x3F);
    }
    *codepoint = decoded == 0x00A0 ? ' ' : static_cast<uint16_t>(decoded);
    return index + length;
}

const OpenSansCondensedGlyph *findGlyph(const OpenSansCondensedFont &font, uint16_t codepoint)
{
    int16_t low = 0;
    int16_t high = static_cast<int16_t>(font.glyphCount) - 1;
    while (low <= high)
    {
        int16_t middle = low + (high - low) / 2;
        const OpenSansCondensedGlyph *glyph = &font.glyphs[middle];
        if (glyph->codepoint == codepoint) { return glyph; }
        if (glyph->codepoint < codepoint) { low = middle + 1; }
        else { high = middle - 1; }
    }
    return nullptr;
}
